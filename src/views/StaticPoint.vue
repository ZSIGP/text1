  <script>
  export default {
    name: 'StaticPoint',
    data() {
      return {
        particlesAnimation: null
      }
    },
    methods: {
      // 初始化粒子背景
      initParticlesBackground() {
        const canvas = this.$refs.particlesCanvas
        const ctx = canvas.getContext('2d')
        
        // 设置画布尺寸
        const resizeCanvas = () => {
          canvas.width = window.innerWidth
          canvas.height = window.innerHeight
        }
        
        resizeCanvas()
        window.addEventListener('resize', resizeCanvas)
        
        // 粒子参数
        const particlesCount = 50
        const particles = []
        
        // 创建粒子
        for (let i = 0; i < particlesCount; i++) {
          particles.push({
            x: Math.random() * canvas.width,
            y: Math.random() * canvas.height,
            radius: Math.random() * 2 + 1,
            color: `rgba(${Math.floor(Math.random() * 100 + 155)}, ${Math.floor(Math.random() * 100 + 155)}, ${Math.floor(Math.random() * 100 + 155)}, ${Math.random() * 0.3 + 0.1})`,
            speedX: Math.random() * 0.5 - 0.25,
            speedY: Math.random() * 0.5 - 0.25
          })
        }
        
        // 动画函数
        const animate = () => {
          ctx.clearRect(0, 0, canvas.width, canvas.height)
          
          // 更新和绘制粒子
          for (let i = 0; i < particlesCount; i++) {
            const p = particles[i]
            
            // 更新位置
            p.x += p.speedX
            p.y += p.speedY
            
            // 边界检查
            if (p.x < 0 || p.x > canvas.width) p.speedX *= -1
            if (p.y < 0 || p.y > canvas.height) p.speedY *= -1
            
            // 绘制粒子
            ctx.beginPath()
            ctx.arc(p.x, p.y, p.radius, 0, Math.PI * 2)
            ctx.fillStyle = p.color
            ctx.fill()
          }
          
          // 继续动画
          this.particlesAnimation = requestAnimationFrame(animate)
        }
        
        // 启动动画
        animate()
      }
    },
    mounted() {
      // 初始化粒子背景
      this.$nextTick(() => {
        this.initParticlesBackground()
      })
    },
    beforeUnmount() {
      // 清除动画
      if (this.particlesAnimation) {
        cancelAnimationFrame(this.particlesAnimation)
      }
      
      // 移除事件监听
      window.removeEventListener('resize', this.resizeCanvas)
    }
  }
  </script>
  
